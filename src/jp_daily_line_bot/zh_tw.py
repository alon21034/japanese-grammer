def to_zh_tw(text: str) -> str:
    out = text
    for src, dst in _PHRASE_REPLACEMENTS:
        out = out.replace(src, dst)
    out = "".join(_CHAR_REPLACEMENTS.get(ch, ch) for ch in out)
    return out
